public class MstrDetails {
	
	private String global;
	private String globalManufacturerName;
	private String obsolete;

	public String getGlobal() {
		return global;
	}

	public void setGlobal(String global) {
		this.global = global;
	}

	public String getGlobalManufacturerName() {
		return globalManufacturerName;
	}

	public void setGlobalManufacturerName(String globalManufacturerName) {
		this.globalManufacturerName = globalManufacturerName;
	}

	public String getObsolete() {
		return obsolete;
	}

	public void setObsolete(String obsolete) {
		this.obsolete = obsolete;
	}

}

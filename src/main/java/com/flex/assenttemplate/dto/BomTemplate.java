package com.flex.assenttemplate.dto;

public class BomTemplate {

	private String flexPartNo;
	private String description;
	private String manufacturer;
	private String mcode;
	private String mpn;
	private String quantity;
	private String emailID;
	private String telno;
	private String assemblyNo;
	private String globalMFR;
	private String obsolete;

	public String getFlexPartNo() {
		return flexPartNo;
	}

	public void setFlexPartNo(String flexPartNo) {
		this.flexPartNo = flexPartNo;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}

	public String getMcode() {
		return mcode;
	}

	public void setMcode(String mcode) {
		this.mcode = mcode;
	}

	public String getMpn() {
		return mpn;
	}

	public void setMpn(String mpn) {
		this.mpn = mpn;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getEmailID() {
		return emailID;
	}

	public void setEmailID(String emailID) {
		this.emailID = emailID;
	}

	public String getTelno() {
		return telno;
	}

	public void setTelno(String telno) {
		this.telno = telno;
	}

	public String getAssemblyNo() {
		return assemblyNo;
	}

	public void setAssemblyNo(String assemblyNo) {
		this.assemblyNo = assemblyNo;
	}

	public String getGlobalMFR() {
		return globalMFR;
	}

	public void setGlobalMFR(String globalMFR) {
		this.globalMFR = globalMFR;
	}

	public String getObsolete() {
		return obsolete;
	}

	public void setObsolete(String obsolete) {
		this.obsolete = obsolete;
	}

}
